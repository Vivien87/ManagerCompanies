

CREATE TABLE COMPANY (
  ID INT GENERATED BY DEFAULT AS IDENTITY,
  NAME VARCHAR(255),
  PRIMARY KEY (ID),
  UNIQUE KEY COMPANY_NAME (NAME)
);



 create table USER
  (
  ID INT GENERATED BY DEFAULT AS IDENTITY,
  NAME VARCHAR(255),
  LASTNAME VARCHAR(255),
  DATE_OF_BIRTH DATE ,
  PRIMARY KEY (ID),

);

CREATE TABLE POSITIONTYPE (
    ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    POSITION VARCHAR (50),
    USERID INT NOT NULL,

    PRIMARY KEY (ID),

   FOREIGN KEY (USERID) REFERENCES USER(ID)
);

CREATE TABLE DEPARTMENT (
  ID INT GENERATED BY DEFAULT AS IDENTITY,
  NAME VARCHAR(255),
  USER_ID INT NOT NULL,
  COMPANY_ID INT NOT NULL,
  DEPARTMENT_CATEGORY VARCHAR(50),

  PRIMARY KEY (ID),

  FOREIGN KEY (USER_ID) REFERENCES USER(ID),
  FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY(ID)

);


CREATE TABLE DEPARTMENTCATEGORY (
    ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR (50),
    PRIMARY KEY (ID),


);
create table DEPARTMENT_CATEGORY(
  DEPARTMENT_ID INT NOT NULL,
  DEPARTMENTCATEGORY_ID INT NOT NULL,
  FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENT(ID),
  FOREIGN KEY (DEPARTMENTCATEGORY_ID) REFERENCES DEPARTMENTCATEGORY(ID)
);